const express = require('express');
const app = express();
const PORT = 3000;

// Route 1: /
app.get('/', (req, res) => {
    res.send('Welcome to Express Server');
});

// Route 2: /about
app.get('/about', (req, res) => {
    res.send('About Page');
});

// Start the server
app.listen(PORT, () => {
    console.log(`Server is running on http://localhost:${PORT}`);
});
